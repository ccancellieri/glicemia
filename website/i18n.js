// Keys that contain trusted HTML markup (bold, links, line breaks)
// These are hardcoded translations, not user input, so safe to render as HTML.
const HTML_KEYS = new Set([
    'chat_bot1_html',
    'start_note_html',
    'disc_text_html',
]);

function setLanguage(lang) {
    document.documentElement.setAttribute('data-lang', lang);
    document.documentElement.setAttribute('lang', lang);

    const t = translations[lang];
    if (!t) return;

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        // Check for _html variant first (trusted HTML content)
        const htmlKey = key + '_html';
        if (t[htmlKey] && HTML_KEYS.has(htmlKey)) {
            el.innerHTML = t[htmlKey]; // eslint-disable-line no-unsanitized/property -- trusted hardcoded translations only
        } else if (t[key]) {
            // For hero_title we need <br> support
            if (key === 'hero_title') {
                el.innerHTML = t[key]; // eslint-disable-line no-unsanitized/property -- hardcoded title with <br> only
            } else {
                el.textContent = t[key];
            }
        }
    });

    // Update active button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });

    // Save preference
    localStorage.setItem('glicemia-lang', lang);

    // Update page title
    const titles = {
        en: "GliceMia \u2014 Intelligent Type 1 Diabetes Companion",
        it: "GliceMia \u2014 Compagno Intelligente per il Diabete Tipo 1",
        es: "GliceMia \u2014 Compa\u00f1ero Inteligente para la Diabetes Tipo 1",
        fr: "GliceMia \u2014 Compagnon Intelligent pour le Diab\u00e8te Type 1",
    };
    document.title = titles[lang] || titles.en;
}

// Initialize
document.addEventListener('DOMContentLoaded', () => {
    // Language buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', () => {
            setLanguage(btn.getAttribute('data-lang'));
        });
    });

    // Detect saved or browser language
    const saved = localStorage.getItem('glicemia-lang');
    if (saved && translations[saved]) {
        setLanguage(saved);
    } else {
        const browserLang = navigator.language.slice(0, 2);
        if (translations[browserLang]) {
            setLanguage(browserLang);
        }
    }
});
